func TestEmitPropertyMethodForm(t *testing.T) {
	src := `package x
import "github.com/legendary-code/godot-go/core"
// @class
type N struct {
	// @extends
	core.Node
}
// @property
func (n *N) GetHealth() int64 { return 0 }
// @property
func (n *N) SetHealth(v int64) {}
`
	out := emitFor(t, src)

	// Method form: codegen does NOT synthesize accessors — the user
	// already wrote them. We just register the property.
	if strings.Contains(out, "{ return n.Health }") {
		t.Errorf("method-form property must not re-synthesize the user's getter:\n%s", out)
	}
	mustContain(t, out, "RegisterClassProperty(gdextension.ClassPropertyDef{")
	mustContain(t, out, `Setter: "set_health",`)
	mustContain(t, out, `Getter: "get_health",`)
}

func TestEmitSignals(t *testing.T) {
	src := `package x
import "github.com/legendary-code/godot-go/core"
// @class
type N struct {
	// @extends
	core.Node
}

// @signals
type Signals interface {
	Damaged(amount int64)
	LeveledUp()
	Tagged(label string)
}
`
	out := emitFor(t, src)

	// Synthesized emit methods on *N — name = signal name, args
	// preserved from the interface declaration.
	mustContain(t, out, "func (n *N) Damaged(amount int64) {")
	mustContain(t, out, "func (n *N) LeveledUp() {")
	mustContain(t, out, "func (n *N) Tagged(label string) {")

	// Per-arg Variant construction + dispatch.
	mustContain(t, out, "arg0 := core.NewVariantInt(amount)")
	mustContain(t, out, "arg0 := core.NewVariantString(label)")
	mustContain(t, out, `gdextension.EmitSignal(n.Ptr(), gdextension.InternStringName("damaged"), args)`)
	mustContain(t, out, `gdextension.EmitSignal(n.Ptr(), gdextension.InternStringName("leveled_up"), nil)`)

	// RegisterClassSignal at SCENE init.
	mustContain(t, out, "RegisterClassSignal(gdextension.ClassSignalDef{")
	mustContain(t, out, `Name:  "damaged",`)
	mustContain(t, out, `Name:  "leveled_up",`)
	mustContain(t, out, `Name:  "tagged",`)

	// The synthesized emit method is Go-only — NOT registered with
	// ClassDB. GDScript callers that want to trigger emission from
	// outside the class use the standard emit_signal API.
	if strings.Contains(out, `Name:  "damaged",
		Call: func(`) {
		t.Errorf("synthesized signal-emit methods must not be ClassDB-registered:\n%s", out)
	}
}

func TestEmitPropertyGroupAndHints(t *testing.T) {
	src := `package x
import "github.com/legendary-code/godot-go/core"
// @class
type N struct {
	// @extends
	core.Node

	// @group("Combat")
	// @export_range(0, 100, 5)
	// @property
	Damage int64

	// @group("Combat")
	// @export_enum("Idle", "Run")
	// @property
	Mode int64

	// @group("Visuals")
	// @subgroup("Texture")
	// @export_file("*.png")
	// @property
	Skin string
}
`
	out := emitFor(t, src)

	// One group registration per group transition; subgroup follows
	// the group it nests under.
	mustContain(t, out, "RegisterClassPropertyGroup(gdextension.ClassPropertyGroupDef{")
	mustContain(t, out, `Name:  "Combat",`)
	mustContain(t, out, `Name:  "Visuals",`)
	mustContain(t, out, "RegisterClassPropertySubgroup(gdextension.ClassPropertyGroupDef{")
	mustContain(t, out, `Name:  "Texture",`)

	// Hints render with the right enum const + payload.
	mustContain(t, out, "Hint:       gdextension.PropertyHintRange,")
	mustContain(t, out, `HintString: "0,100,5",`)
	mustContain(t, out, "Hint:       gdextension.PropertyHintEnum,")
	mustContain(t, out, `HintString: "Idle,Run",`)
	mustContain(t, out, "Hint:       gdextension.PropertyHintFile,")
	mustContain(t, out, `HintString: "*.png",`)
}

