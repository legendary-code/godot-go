package main

import (
	"bytes"
	"strings"
	"testing"
)

// emitFor parses src, runs discovery, and returns the emitter's output.
// Intentionally goes through the same pipeline `godot-go` uses at runtime
// — that way the test catches regressions anywhere along the pipe, not
// just in emit.go.
func emitFor(t *testing.T, src string) string {
	t.Helper()
	d := mustDiscover(t, src)
	var buf bytes.Buffer
	if err := emit(&buf, d); err != nil {
		t.Fatalf("emit: %v", err)
	}
	return buf.String()
}

func TestEmitMainClassWithMethod(t *testing.T) {
	src := `package mypkg
import "github.com/legendary-code/godot-go/core"
type MyNode struct { core.Node }
func (n *MyNode) Hello() {}
`
	out := emitFor(t, src)

	mustContain(t, out, "package mypkg")
	mustContain(t, out, "// Code generated by godot-go; DO NOT EDIT.")
	mustContain(t, out, `Name:      "MyNode",`)
	mustContain(t, out, `Parent:    "Node",`)
	// Side-table helpers must use the lowercased class prefix.
	mustContain(t, out, "myNodeInstancesMu")
	mustContain(t, out, "myNodeInstances   = map[uintptr]*MyNode{}")
	// Method registration: Go name `Hello`, Godot name `hello`.
	mustContain(t, out, `Name:  "hello",`)
	mustContain(t, out, "n.Hello()")
	// Auto-init wiring at SCENE level.
	mustContain(t, out, "RegisterInitCallback(gdextension.InitLevelScene, registerMyNode)")
	mustContain(t, out, `UnregisterClass("MyNode")`)
}

func TestEmitNoMethods(t *testing.T) {
	src := `package x
import "github.com/legendary-code/godot-go/core"
type Lonely struct { core.Node }
`
	out := emitFor(t, src)
	if strings.Contains(out, "RegisterClassMethod") {
		t.Errorf("class with no methods shouldn't emit RegisterClassMethod call:\n%s", out)
	}
	mustContain(t, out, "registerLonely")
}

func TestEmitRejectsStaticMethod(t *testing.T) {
	src := `package x
import "github.com/legendary-code/godot-go/core"
type N struct { core.Node }
func (N) Foo() {}
`
	d := mustDiscover(t, src)
	var buf bytes.Buffer
	err := emit(&buf, d)
	if err == nil {
		t.Fatalf("expected emit to reject static method")
	}
	if !strings.Contains(err.Error(), "static methods not supported yet") {
		t.Errorf("unexpected error: %v", err)
	}
}

