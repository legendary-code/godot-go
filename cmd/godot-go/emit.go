package main

import (
	"bytes"
	"fmt"
	"go/format"
	"io"
	"path/filepath"
	"strings"
	"text/template"
)

// emit renders the binding file for the discovered class to w. Phase 5c
// scope: emit registration glue + instance methods that take no arguments
// and return no values. Methods with non-trivial signatures are flagged
// as unsupported (Phase 5d will fold in the marshalling layer); static
// and virtual-candidate methods are flagged as unsupported (Phase 5e).
//
// The emitter writes gofmt'd source so diffs against hand-written
// references stay clean. Iteration order is deterministic — methods are
// emitted in source-position order, mirroring the discovered slice.
func emit(w io.Writer, d *discovered) error {
	if d.MainClass == nil {
		return fmt.Errorf("no main class to emit")
	}

	supported, err := classifyForEmit(d.MainClass.Methods)
	if err != nil {
		return err
	}

	data := emitData{
		PackageName: d.PackageName,
		SourceFile:  filepath.Base(d.FilePath),
		Class:       d.MainClass.Name,
		Parent:      d.MainClass.Parent,
		Lower:       lowerFirst(d.MainClass.Name),
		Methods:     supported,
	}

	var buf bytes.Buffer
	if err := bindingTemplate.Execute(&buf, data); err != nil {
		return fmt.Errorf("template: %w", err)
	}
	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		return fmt.Errorf("gofmt emitted source: %w\n--- raw ---\n%s", err, buf.String())
	}
	_, err = w.Write(formatted)
	return err
}

type emitData struct {
	PackageName string
	SourceFile  string
	Class       string
	Parent      string
	Lower       string // class name with first rune lowercased — used for unexported helpers
	Methods     []emitMethod
}

type emitMethod struct {
	GoName    string
	GodotName string
}

func classifyForEmit(methods []*methodInfo) ([]emitMethod, error) {
	var out []emitMethod
	for _, m := range methods {
		switch m.Kind {
		case methodStatic:
			return nil, fmt.Errorf("static methods not supported yet (deferred to Phase 5e): %s", m.GoName)
		case methodVirtualCandidate:
			return nil, fmt.Errorf("virtual-candidate methods not supported yet (deferred to Phase 5e): %s", m.GoName)
		}
		ft := m.Decl.Type
		if ft.Params != nil && len(ft.Params.List) > 0 {
			return nil, fmt.Errorf("methods with parameters not supported yet (deferred to Phase 5d): %s", m.GoName)
		}
		if ft.Results != nil && len(ft.Results.List) > 0 {
			return nil, fmt.Errorf("methods with return values not supported yet (deferred to Phase 5d): %s", m.GoName)
		}
		out = append(out, emitMethod{GoName: m.GoName, GodotName: m.GodotName})
	}
	return out, nil
}

func lowerFirst(s string) string {
	if s == "" {
		return s
	}
	return strings.ToLower(s[:1]) + s[1:]
}

// bindingTemplate produces the same shape as the hand-written
// examples/smoke/mynode_bindings.go reference, with an init() that
// auto-registers the class at SCENE level (and unregisters at SCENE
// deinit) so the user's main package doesn't need to know about it.
var bindingTemplate = template.Must(template.New("binding").Parse(`// Code generated by godot-go; DO NOT EDIT.
// Source: {{.SourceFile}}

package {{.PackageName}}

import (
	"sync"
	"unsafe"

	"github.com/legendary-code/godot-go/internal/gdextension"
)

// Per-instance side table. The void* value the host hands back to us as
// p_instance is the small integer id we returned from Construct, not a
// Go pointer (cgo forbids storing Go pointers in C-visible memory).
var (
	{{.Lower}}InstancesMu sync.Mutex
	{{.Lower}}Instances   = map[uintptr]*{{.Class}}{}
	{{.Lower}}NextID      uintptr
)

func register{{.Class}}Instance(n *{{.Class}}) unsafe.Pointer {
	{{.Lower}}InstancesMu.Lock()
	{{.Lower}}NextID++
	id := {{.Lower}}NextID
	{{.Lower}}Instances[id] = n
	{{.Lower}}InstancesMu.Unlock()
	return unsafe.Pointer(id)
}

func lookup{{.Class}}Instance(handle unsafe.Pointer) *{{.Class}} {
	{{.Lower}}InstancesMu.Lock()
	defer {{.Lower}}InstancesMu.Unlock()
	return {{.Lower}}Instances[uintptr(handle)]
}

func release{{.Class}}Instance(handle unsafe.Pointer) {
	{{.Lower}}InstancesMu.Lock()
	defer {{.Lower}}InstancesMu.Unlock()
	delete({{.Lower}}Instances, uintptr(handle))
}

func register{{.Class}}() {
	gdextension.RegisterClass(gdextension.ClassDef{
		Name:      "{{.Class}}",
		Parent:    "{{.Parent}}",
		IsExposed: true,

		Construct: func() (gdextension.ObjectPtr, unsafe.Pointer) {
			n := &{{.Class}}{}
			parent := gdextension.ConstructObject(gdextension.InternStringName("{{.Parent}}"))
			n.BindPtr(parent)
			return parent, register{{.Class}}Instance(n)
		},

		Free: func(instance unsafe.Pointer) {
			release{{.Class}}Instance(instance)
		},
	})
{{range .Methods}}
	gdextension.RegisterClassMethod(gdextension.ClassMethodDef{
		Class: "{{$.Class}}",
		Name:  "{{.GodotName}}",
		Call: func(instance unsafe.Pointer, _ []gdextension.VariantPtr, _ gdextension.VariantPtr) gdextension.CallErrorType {
			n := lookup{{$.Class}}Instance(instance)
			if n == nil {
				return gdextension.CallErrorInstanceIsNull
			}
			n.{{.GoName}}()
			return gdextension.CallErrorOK
		},
		PtrCall: func(instance unsafe.Pointer, _ unsafe.Pointer, _ unsafe.Pointer) {
			if n := lookup{{$.Class}}Instance(instance); n != nil {
				n.{{.GoName}}()
			}
		},
	})
{{end}}}

func init() {
	gdextension.RegisterInitCallback(gdextension.InitLevelScene, register{{.Class}})
	gdextension.RegisterDeinitCallback(gdextension.InitLevelScene, func() {
		gdextension.UnregisterClass("{{.Class}}")
	})
}
`))
