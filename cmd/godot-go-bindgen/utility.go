package main

import (
	"bytes"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"sort"
	"strings"
)

// emitUtilityFunctions writes utility.gen.go in the bindings dir with one
// Go function per non-vararg entry in api.UtilityFunctions. Each is a
// free-standing ptrcall through variant_get_ptr_utility_function — no
// `self`, no method bind. The cached fn pointers go through sync.OnceValue
// so the first call to any given utility pays for the lookup and the rest
// are pointer loads.
func emitUtilityFunctions(api *API, cfg *genConfig) error {
	imports := map[string]bool{
		"sync":   true,
		"unsafe": true,
		cfg.ModulePath + "/gdextension": true,
	}

	type funView struct {
		Name      string // pascal-cased Go name
		GodotName string
		Sig       string // "Foo(x float32) float32"
		Body      string
		Cache     cacheVar
	}

	var emitted []funView
	skipped := 0
	for i := range api.UtilityFunctions {
		uf := &api.UtilityFunctions[i]
		if uf.IsVararg {
			skipped++
			continue
		}
		// Adapt utility Arguments → ClassArguments so we can reuse the
		// engine-class boundary code (the only differences are the unused
		// Meta + DefaultValue fields).
		cargs := make([]ClassArgument, len(uf.Arguments))
		for j, a := range uf.Arguments {
			cargs[j] = ClassArgument{Name: a.Name, Type: a.Type, DefaultValue: a.DefaultValue}
		}
		var rv *ClassReturnVal
		if uf.ReturnType != "" {
			rv = &ClassReturnVal{Type: uf.ReturnType}
		}

		rPlan, rImports, ok := classReturnPlan(cfg.Package, rv)
		if !ok {
			skipped++
			continue
		}
		prep, argsExpr, aImports, ok := buildClassArgs(cfg.Package, cargs)
		if !ok {
			skipped++
			continue
		}
		for ip := range rImports {
			imports[ip] = true
		}
		for ip := range aImports {
			imports[ip] = true
		}

		goName := pascal(uf.Name)
		cacheName := "util" + goName
		cache := cacheVar{
			Name: cacheName,
			Type: "= sync.OnceValue(func() gdextension.PtrUtilityFunction {\n" +
				fmt.Sprintf("\treturn gdextension.GetPtrUtilityFunction(gdextension.InternStringName(%q), %d)\n", uf.Name, uf.Hash) +
				"})",
		}

		sig := fmt.Sprintf("%s(%s)", goName, paramSigCtx(cfg.Package, cargs))
		if rPlan.GoType != "" {
			sig += " " + rPlan.GoType
		}

		var b strings.Builder
		if rPlan.DeclRet != "" {
			b.WriteString(rPlan.DeclRet)
			b.WriteString("\n\t")
		}
		if rPlan.Finalize != "" {
			b.WriteString(rPlan.Finalize)
			b.WriteString("\n\t")
		}
		if prep != "" {
			b.WriteString(prep)
		}
		fmt.Fprintf(&b, "gdextension.CallPtrUtilityFunction(%s(), %s, %s)",
			cacheName, rPlan.RetArg, argsExpr)
		if rPlan.RetExpr != "" {
			b.WriteString("\n\t")
			b.WriteString(rPlan.RetExpr)
		}

		emitted = append(emitted, funView{
			Name:      goName,
			GodotName: uf.Name,
			Sig:       sig,
			Body:      b.String(),
			Cache:     cache,
		})
	}

	// Stable order: by Godot name (already API-declaration order, but sort
	// to keep output deterministic against any future re-shuffling of the
	// json input).
	sort.Slice(emitted, func(i, j int) bool {
		return emitted[i].GodotName < emitted[j].GodotName
	})

	var buf bytes.Buffer
	buf.WriteString("// Code generated by godot-go-bindgen. DO NOT EDIT.\n\n")
	fmt.Fprintf(&buf, "package %s\n\n", cfg.Package)

	buf.WriteString("import (\n")
	importList := make([]string, 0, len(imports))
	for ip := range imports {
		importList = append(importList, ip)
	}
	sort.Strings(importList)
	for _, ip := range importList {
		fmt.Fprintf(&buf, "\t%q\n", ip)
	}
	buf.WriteString(")\n\n")

	buf.WriteString("// Lazily-resolved utility-function pointers. Each first-call performs\n")
	buf.WriteString("// the host lookup; subsequent calls are a plain pointer load.\n")
	buf.WriteString("var (\n")
	for _, f := range emitted {
		fmt.Fprintf(&buf, "\t%s %s\n", f.Cache.Name, f.Cache.Type)
	}
	buf.WriteString(")\n\n")

	for _, f := range emitted {
		fmt.Fprintf(&buf, "// %s mirrors Godot's %s utility function.\n", f.Name, f.GodotName)
		fmt.Fprintf(&buf, "func %s {\n\t%s\n}\n\n", f.Sig, f.Body)
	}

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		broken := filepath.Join(cfg.Dir, "utility.gen.go.broken")
		_ = os.WriteFile(broken, buf.Bytes(), 0o644)
		return fmt.Errorf("gofmt utility: %w (raw at %s)", err, broken)
	}

	out := filepath.Join(cfg.Dir, "utility.gen.go")
	if err := os.WriteFile(out, formatted, 0o644); err != nil {
		return err
	}

	fmt.Fprintf(os.Stderr, "godot-go-bindgen: emitted %d utility functions (%d skipped)\n",
		len(emitted), skipped)
	return nil
}

