// runtimeSubpkgTmpl renders <cfg.Dir>/runtime/runtime.gen.go. Substitutions:
// .BindingsAlias (the Go package name to use for the parent import — same
// as cfg.Package), .BindingsImport (the parent's full import path),
// .ModulePath (the framework module path).
var runtimeSubpkgTmpl = template.Must(template.New("runtime_subpkg").Parse(`// Code generated by godot-go-bindgen. DO NOT EDIT.

// Package runtime layers user-facing conveniences on top of the bindings
// in the parent package. IsEditorHint reads the Engine singleton; the
// PrintInfo / PrintVerbose / PrintWarning / PrintError families route
// through Godot's Output dock at the matching severity level (Warning
// and Error capture caller info via the gdextension print_warning /
// print_error interface entries; Info and Verbose route through the
// print / print_verbose utility functions). RunOnMain / DrainMain
// / IsMainThread re-export the framework's main-thread queue.
package runtime

import (
	"fmt"
	goruntime "runtime"
	"sync"
	"unsafe"

	"{{.ModulePath}}/gdextension"
	{{.BindingsAlias}} "{{.BindingsImport}}"
)

// IsEditorHint mirrors GDScript's Engine.is_editor_hint(). Returns true while
// editor-only code paths are running (e.g. inspector rebuilds), false during
// play-mode and shipped builds. Distinct from the @editor codegen doctag,
// which controls whether a class is registered at all.
//
// Cheap — Engine singleton resolved once and cached. The underlying call is
// a single ptrcall via the engine's MethodBind cache.
func IsEditorHint() bool { return engineSingleton().IsEditorHint() }

var engineSingleton = sync.OnceValue(func() *{{.BindingsAlias}}.Engine {
	return {{.BindingsAlias}}.EngineSingleton()
})

// utilPrint / utilPrintVerbose cache the cross-version ptr lookups for
// the print / print_verbose utility functions. Both share the
// vararg-Variant hash 2648703342 across Godot 4.4–4.6.
var (
	utilPrint = sync.OnceValue(func() gdextension.PtrUtilityFunction {
		return gdextension.GetPtrUtilityFunction(gdextension.InternStringName("print"), 2648703342)
	})
	utilPrintVerbose = sync.OnceValue(func() gdextension.PtrUtilityFunction {
		return gdextension.GetPtrUtilityFunction(gdextension.InternStringName("print_verbose"), 2648703342)
	})
)

// callPrintUtility invokes a print-shaped vararg utility function with
// a single string argument. The String is built via the bindings'
// NewVariantString (Go-string → Variant), passed as a single TypePtr
// to CallPtrUtilityFunction, then destroyed once the call returns.
func callPrintUtility(fn gdextension.PtrUtilityFunction, msg string) {
	v := {{.BindingsAlias}}.NewVariantString(msg)
	defer v.Destroy()
	args := [...]gdextension.TypePtr{
		gdextension.TypePtr(unsafe.Pointer(&v)),
	}
	gdextension.CallPtrUtilityFunction(fn, gdextension.TypePtr(nil), args[:])
}

// PrintInfo logs args to Godot's Output dock at the standard info level
// (white text). Routes through Godot's print() utility function — same
// thing GDScript's print(args) ultimately calls. No caller info; use
// PrintWarning / PrintError when you want the file:line surfaced in the
// Output dock entry.
func PrintInfo(args ...any) {
	callPrintUtility(utilPrint(), fmt.Sprint(args...))
}

// PrintInfof is PrintInfo with a format string.
func PrintInfof(format string, args ...any) {
	callPrintUtility(utilPrint(), fmt.Sprintf(format, args...))
}

// PrintVerbose logs args via Godot's print_verbose() utility — only
// shows up when the engine is started with --verbose (or
// engine.print_to_stdout enabled in editor mode). Useful for "noisy"
// diagnostic logging that should be opt-in.
//
// GDExtension doesn't expose print_debug directly (it's a GDScript-
// only utility). PrintVerbose is the closest analog.
func PrintVerbose(args ...any) {
	callPrintUtility(utilPrintVerbose(), fmt.Sprint(args...))
}

// PrintVerbosef is PrintVerbose with a format string.
func PrintVerbosef(format string, args ...any) {
	callPrintUtility(utilPrintVerbose(), fmt.Sprintf(format, args...))
}

// PrintWarning logs args to Godot's Output dock at warning level
// (yellow text) with caller info. Routes through gdextension's
// print_warning interface entry — same path the engine uses internally
// for its own warning messages.
func PrintWarning(args ...any) {
	fn, file, line := callerInfo(2)
	gdextension.PrintWarning(fmt.Sprint(args...), fn, file, line, false)
}

// PrintWarningf is PrintWarning with a format string.
func PrintWarningf(format string, args ...any) {
	fn, file, line := callerInfo(2)
	gdextension.PrintWarning(fmt.Sprintf(format, args...), fn, file, line, false)
}

// PrintError logs args to Godot's Output dock at error level (red text)
// with caller info. Routes through gdextension's print_error interface
// entry.
func PrintError(args ...any) {
	fn, file, line := callerInfo(2)
	gdextension.PrintError(fmt.Sprint(args...), fn, file, line, false)
}

// PrintErrorf is PrintError with a format string.
func PrintErrorf(format string, args ...any) {
	fn, file, line := callerInfo(2)
	gdextension.PrintError(fmt.Sprintf(format, args...), fn, file, line, false)
}

func callerInfo(skip int) (fn, file string, line int32) {
	pc, f, l, ok := goruntime.Caller(skip)
	if !ok {
		return "<unknown>", "<unknown>", 0
	}
	name := "<unknown>"
	if fp := goruntime.FuncForPC(pc); fp != nil {
		name = fp.Name()
	}
	return name, f, int32(l)
}

// IsMainThread reports whether the calling goroutine is on the engine's
// main thread.
func IsMainThread() bool { return gdextension.IsMainThread() }

// RunOnMain schedules f on the engine's main thread (inline if already on
// main, queued otherwise).
func RunOnMain(f func()) { gdextension.RunOnMain(f) }

// DrainMain executes every func currently queued via RunOnMain. Must be
// called from the main thread.
func DrainMain() { gdextension.DrainMain() }

// LoadEditorDocXML loads class-XML docs into the editor's help DB,
// gated on IsEditorHint. Godot's GDExtensionEditorHelp::load_xml_buffer
// (the underlying interface entry point) is editor-only — its
// internal callback pointer is null in headless / deployed-game
// runtimes, and calling it logs a noisy "editor_help_load_xml_buffer
// is null" error from the engine. Skipping the call when not in the
// editor avoids that noise; the docs aren't useful at runtime anyway.
//
// Codegen emits register<Class>() calls into this wrapper so users
// don't have to think about the gating themselves.
func LoadEditorDocXML(xml string) {
	if !IsEditorHint() {
		return
	}
	gdextension.LoadEditorDocXML(xml)
}
`))
