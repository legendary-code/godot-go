package main

import (
	"bytes"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"strings"
)

// emitNativeStructures writes native.gen.go in the bindings dir: one Go
// struct per entry in api.NativeStructures, decoded from the C-ish format
// strings.
//
// These structs are layout-stable mirrors of the host's C++ POD types —
// extension callbacks (physics ray results, text shaping caret info, …) write
// into them across the cgo boundary, so field ordering and types must match.
// "Layout-stable" here relies on:
//   - Godot builtins are opaque [N]byte arrays (already stable),
//   - C `int` mapped to int32 (Windows / Linux x86_64),
//   - `Object *` mapped to gdextension.ObjectPtr (pointer-sized opaque),
//   - C++-scoped enums collapsed to int64 with a comment naming the original.
func emitNativeStructures(api *API, cfg *genConfig) error {

	// Collect the set of names used as same-package references so they
	// resolve when we hit them in another struct's format string.
	known := map[string]bool{}
	for i := range api.NativeStructures {
		known[api.NativeStructures[i].Name] = true
	}

	var buf bytes.Buffer
	imports := map[string]bool{}

	type fieldOut struct {
		Name    string
		GoType  string
		Comment string // appended after the field if non-empty
	}
	type structOut struct {
		Name   string
		Fields []fieldOut
		Skip   string // populated when one or more fields couldn't be resolved
	}

	var emitted []structOut
	for i := range api.NativeStructures {
		n := &api.NativeStructures[i]
		out := structOut{Name: n.Name}
		for _, raw := range strings.Split(n.Format, ";") {
			raw = strings.TrimSpace(raw)
			if raw == "" {
				continue
			}
			fname, ftype, isArray, arrSize, ok := parseNativeField(raw)
			if !ok {
				out.Skip = fmt.Sprintf("unparseable field %q", raw)
				break
			}
			gtype, imps, comment, ok := mapNativeType(ftype, known, cfg)
			if !ok {
				out.Skip = fmt.Sprintf("unsupported field type %q in %q", ftype, raw)
				break
			}
			for ip := range imps {
				imports[ip] = true
			}
			if isArray {
				gtype = fmt.Sprintf("[%d]%s", arrSize, gtype)
			}
			out.Fields = append(out.Fields, fieldOut{
				Name:    pascal(fname),
				GoType:  gtype,
				Comment: comment,
			})
		}
		emitted = append(emitted, out)
	}

	buf.WriteString("// Code generated by godot-go-bindgen. DO NOT EDIT.\n\n")
	fmt.Fprintf(&buf, "package %s\n\n", cfg.Package)

	if len(imports) > 0 {
		buf.WriteString("import (\n")
		ips := make([]string, 0, len(imports))
		for ip := range imports {
			ips = append(ips, ip)
		}
		sortStrings(ips)
		for _, ip := range ips {
			fmt.Fprintf(&buf, "\t%q\n", ip)
		}
		buf.WriteString(")\n\n")
	}

	skipped := 0
	for _, s := range emitted {
		if s.Skip != "" {
			fmt.Fprintf(&buf, "// %s skipped: %s\n\n", s.Name, s.Skip)
			skipped++
			continue
		}
		fmt.Fprintf(&buf, "// %s mirrors the host's native_structures entry of the same name.\n", s.Name)
		fmt.Fprintf(&buf, "type %s struct {\n", s.Name)
		for _, f := range s.Fields {
			fmt.Fprintf(&buf, "\t%s %s", f.Name, f.GoType)
			if f.Comment != "" {
				fmt.Fprintf(&buf, " // %s", f.Comment)
			}
			buf.WriteString("\n")
		}
		buf.WriteString("}\n\n")
	}

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		broken := filepath.Join(cfg.Dir, "native.gen.go.broken")
		_ = os.WriteFile(broken, buf.Bytes(), 0o644)
		return fmt.Errorf("gofmt native: %w (raw at %s)", err, broken)
	}

	out := filepath.Join(cfg.Dir, "native.gen.go")
	if err := os.WriteFile(out, formatted, 0o644); err != nil {
		return err
	}

	fmt.Fprintf(os.Stderr, "godot-go-bindgen: emitted %d native structures (%d skipped)\n",
		len(emitted)-skipped, skipped)
	return nil
}

// parseNativeField extracts (name, type, isArray, arraySize) from one C-ish
// declaration like "Vector2 travel" or "uint8_t count = 0" or
// "PhysicsServer3DExtensionMotionCollision collisions[32]". The default-value
// suffix (after `=`) is dropped; Go zero values stand in.
func parseNativeField(raw string) (name, typeStr string, isArray bool, arraySize int, ok bool) {
	// Strip default-value tail.
	if eq := strings.IndexByte(raw, '='); eq >= 0 {
		raw = strings.TrimSpace(raw[:eq])
	}
	// Pull the trailing identifier (with optional [N]) off the end.
	// Handle "Object *collider": last ident is "collider", type is "Object *".
	last := strings.LastIndexAny(raw, " *")
	if last < 0 {
		return "", "", false, 0, false
	}
	typeStr = strings.TrimSpace(raw[:last+1])
	tail := strings.TrimSpace(raw[last+1:])
	if tail == "" {
		return "", "", false, 0, false
	}
	// Handle "name[32]".
	if lb := strings.IndexByte(tail, '['); lb >= 0 {
		rb := strings.IndexByte(tail, ']')
		if rb < 0 || rb <= lb {
			return "", "", false, 0, false
		}
		size := tail[lb+1 : rb]
		var n int
		_, err := fmt.Sscanf(size, "%d", &n)
		if err != nil || n <= 0 {
			return "", "", false, 0, false
		}
		isArray = true
		arraySize = n
		tail = tail[:lb]
	}
	return tail, typeStr, isArray, arraySize, true
}

// mapNativeType converts a C-ish type token (e.g. "Vector2", "uint8_t",
// "real_t", "Object *", "TextServer::Direction") to a Go type plus any
// imports + an optional trailing comment for non-obvious mappings.
//
// Returns ok=false if the type isn't recognized — the caller flags the whole
// struct as skipped rather than emit a half-broken file.
//
// All bindings live in one package now (cfg.Package), so cross-class refs
// don't need a package prefix and don't add an import.
func mapNativeType(c string, known map[string]bool, cfg *genConfig) (goTypeOut string, imports map[string]bool, comment string, ok bool) {
	imports = map[string]bool{}
	c = strings.TrimSpace(c)

	// Pointer types (Object *) → gdextension.ObjectPtr. Anything else with a
	// star is unsupported until a use case arrives.
	if strings.HasSuffix(c, "*") {
		base := strings.TrimSpace(strings.TrimSuffix(c, "*"))
		if base == "Object" {
			imports[cfg.ModulePath+"/internal/gdextension"] = true
			return "gdextension.ObjectPtr", imports, "Object *", true
		}
		return "", imports, "", false
	}

	// C++-scoped enums (TextServer::Direction, …) collapse to int64 — same
	// rule used elsewhere for enum-typed signatures.
	if strings.Contains(c, "::") {
		return "int64", imports, c, true
	}

	switch c {
	case "bool":
		return "bool", imports, "", true
	case "int":
		// C `int` on Windows / Linux x86_64 is 32-bit; that's the layout
		// the host writes. Mismatch on exotic platforms is documented in
		// the package preamble.
		return "int32", imports, "C int", true
	case "uint8_t":
		return "uint8", imports, "", true
	case "uint16_t":
		return "uint16", imports, "", true
	case "int32_t":
		return "int32", imports, "", true
	case "uint32_t":
		return "uint32", imports, "", true
	case "int64_t":
		return "int64", imports, "", true
	case "uint64_t":
		return "uint64", imports, "", true
	case "float":
		return "float32", imports, "", true
	case "double":
		return "float64", imports, "", true
	case "real_t":
		// Single-precision build target.
		return "float32", imports, "real_t", true
	case "String", "StringName", "NodePath", "Variant":
		// goType maps these to Go `string` / Variant for the user-facing
		// API, but native structs hold the opaque layout type. Same
		// package, no import.
		return c, imports, "", true
	}

	// Same-package native struct ref (e.g. ObjectID inside another struct).
	if known[c] {
		return c, imports, "", true
	}

	// Fall through to the builtin types (Vector2, Rect2, RID, StringName,
	// …). Same package now, so just bare names.
	if g, k := goType(c); k == kindBuiltin {
		return g, imports, "", true
	}

	return "", imports, "", false
}
