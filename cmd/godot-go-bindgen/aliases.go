package main

import (
	"bytes"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
)

// engineClassRoots is the curated list of engine-class types we re-export
// onto the godot/ facade. These are the inheritance pivots users actually
// compose with — adding leaves like AudioStreamMP3 buys very little and
// bloats the facade.
var engineClassRoots = []string{
	"Object",
	"RefCounted",
	"Resource",
	"Node",
	"Node2D",
	"Node3D",
	"CanvasItem",
	"Control",
	"Container",
	"MainLoop",
	"SceneTree",
	"Window",
	"Viewport",
	"Engine",
	"OS",
	"ProjectSettings",
	"Input",
	"PackedScene",
	"Script",
}

// engineSingletonRoots names a subset of engineClassRoots that are also
// host-registered singletons (extension_api.json#singletons). Each gets a
// `<Name>Singleton` accessor in core/ that we re-export.
var engineSingletonRoots = []string{
	"Engine",
	"OS",
	"ProjectSettings",
	"Input",
}

// emitAliases writes <godotDir>/aliases.gen.go, a small re-export shim that
// promotes the high-traffic builtin types (from variant/) and engine-class
// roots (from core/) onto the top-level `godot` package. Users typically
// import "godot" and reach for `godot.Vector2` or `godot.Node` rather than
// fishing through the implementation packages.
//
// The engine-class root list is curated rather than exhaustive — re-exporting
// every one of the 944 core classes would clutter `godot/` and slow IDE
// completion. Only the types users actually compose into their game code
// (the inheritance pivots: Node, Resource, Control, etc.) belong here.
func emitAliases(builtins, engineRoots []string, godotDir string) error {
	// Restrict to the names we actually emitted, in the listed canonical order.
	want := []string{
		"Variant",
		"String", "StringName", "NodePath",
		"Vector2", "Vector2i", "Vector3", "Vector3i", "Vector4", "Vector4i",
		"Rect2", "Rect2i",
		"Transform2D", "Transform3D",
		"Plane", "Quaternion", "AABB", "Basis", "Projection",
		"Color",
		"RID",
		"Callable", "Signal",
		"Dictionary", "Array",
		"PackedByteArray", "PackedInt32Array", "PackedInt64Array",
		"PackedFloat32Array", "PackedFloat64Array",
		"PackedStringArray",
		"PackedVector2Array", "PackedVector3Array", "PackedVector4Array",
		"PackedColorArray",
	}
	have := map[string]bool{"Variant": true} // hand-written, always present
	for _, b := range builtins {
		have[b] = true
	}

	var b bytes.Buffer
	b.WriteString(`// Code generated by godot-go-bindgen. DO NOT EDIT.

// Package godot is the public facade for godot-go users. It re-exports the
// high-traffic builtin types from variant/ and the engine-class roots from
// core/ so user code can `+"`import \"github.com/legendary-code/godot-go/godot\"`"+`
// and reach for godot.Vector2 or godot.Node directly.
package godot

import (
	"github.com/legendary-code/godot-go/core"
	"github.com/legendary-code/godot-go/variant"
)

`)
	b.WriteString("// Builtin types from variant/.\ntype (\n")
	for _, name := range want {
		if !have[name] {
			continue
		}
		fmt.Fprintf(&b, "\t%s = variant.%s\n", name, name)
	}
	b.WriteString(")\n\n")

	if len(engineRoots) > 0 {
		b.WriteString("// Engine-class roots from core/. The full hierarchy lives in core/ and\n// editor/; this is the curated set users compose with directly.\ntype (\n")
		for _, name := range engineRoots {
			fmt.Fprintf(&b, "\t%s = core.%s\n", name, name)
		}
		b.WriteString(")\n\n")

		// Singleton accessors get re-exported as functions, not type
		// aliases — they're nullary OnceValues in core/.
		b.WriteString("// Engine singletons. Each lazily resolves on first call.\nvar (\n")
		for _, name := range engineSingletonRoots {
			fmt.Fprintf(&b, "\t%sSingleton = core.%sSingleton\n", name, name)
		}
		b.WriteString(")\n")
	}

	formatted, err := format.Source(b.Bytes())
	if err != nil {
		_ = os.WriteFile(filepath.Join(godotDir, "aliases.gen.go.broken"), b.Bytes(), 0o644)
		return fmt.Errorf("gofmt: %w\n%s", err, b.String())
	}

	out := filepath.Join(godotDir, "aliases.gen.go")
	return os.WriteFile(out, formatted, 0o644)
}
