package main

import (
	"bytes"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"sort"
)

// emitAliases writes <godotDir>/aliases.gen.go, a re-export shim that
// promotes every emitted builtin (from variant/) and every engine class
// (from core/ and editor/) onto the top-level `godot` package. Singleton
// accessors get the same treatment.
//
// Everything is data-driven from extension_api.json — no hard-coded names.
// A regen against a newer API automatically picks up new classes /
// singletons / builtins, and won't leave stale references to types the
// API has dropped.
func emitAliases(api *API, builtins []string, godotDir string) error {
	type entry struct{ Name, Pkg string }

	// Engine classes: every entry in api.Classes, qualified by its api_type.
	classes := make([]entry, 0, len(api.Classes))
	pkgsUsed := map[string]bool{}
	for i := range api.Classes {
		c := &api.Classes[i]
		pkg := c.APIType
		if pkg == "" {
			pkg = "core"
		}
		classes = append(classes, entry{Name: c.Name, Pkg: pkg})
		pkgsUsed[pkg] = true
	}

	// Singletons: each registered singleton points at a class; the accessor
	// emitted by the engine sweep is named `<ClassName>Singleton` in whichever
	// package the class lives in. Skip any singleton whose type isn't an
	// emitted class (defensive — shouldn't happen against a sane API).
	singletons := make([]entry, 0, len(api.Singletons))
	for i := range api.Singletons {
		s := &api.Singletons[i]
		cls := api.FindClass(s.Type)
		if cls == nil {
			continue
		}
		pkg := cls.APIType
		if pkg == "" {
			pkg = "core"
		}
		singletons = append(singletons, entry{Name: s.Type, Pkg: pkg})
	}

	// Imports — variant always (Variant is hand-written there); core/editor
	// only when classes from those packages exist.
	imports := []string{"github.com/legendary-code/godot-go/variant"}
	for pkg := range pkgsUsed {
		imports = append(imports, "github.com/legendary-code/godot-go/"+pkg)
	}
	sort.Strings(imports)

	var b bytes.Buffer
	b.WriteString(`// Code generated by godot-go-bindgen. DO NOT EDIT.

// Package godot is the public facade for godot-go users. It re-exports
// every emitted builtin (from variant/) and every engine class (from
// core/ and editor/) so user code can ` + "`import \"github.com/legendary-code/godot-go/godot\"`" + `
// and reach for godot.Vector2 or godot.Node3D directly.
package godot

import (
`)
	for _, ip := range imports {
		fmt.Fprintf(&b, "\t%q\n", ip)
	}
	b.WriteString(")\n\n")

	// Builtin types. Variant is hand-written in variant/ and always present;
	// the rest come from the bindgen sweep in API-declaration order.
	b.WriteString("// Builtin types from variant/.\ntype (\n")
	b.WriteString("\tVariant = variant.Variant\n")
	for _, name := range builtins {
		fmt.Fprintf(&b, "\t%s = variant.%s\n", name, name)
	}
	b.WriteString(")\n\n")

	if len(classes) > 0 {
		b.WriteString("// Engine classes from core/ and editor/. One alias per emitted class —\n// keeping this data-driven means a future api regen picks up additions and\n// drops removals automatically.\ntype (\n")
		for _, e := range classes {
			fmt.Fprintf(&b, "\t%s = %s.%s\n", e.Name, e.Pkg, e.Name)
		}
		b.WriteString(")\n\n")
	}

	if len(singletons) > 0 {
		b.WriteString("// Engine singletons. Each lazily resolves on first call.\nvar (\n")
		for _, s := range singletons {
			fmt.Fprintf(&b, "\t%sSingleton = %s.%sSingleton\n", s.Name, s.Pkg, s.Name)
		}
		b.WriteString(")\n")
	}

	formatted, err := format.Source(b.Bytes())
	if err != nil {
		_ = os.WriteFile(filepath.Join(godotDir, "aliases.gen.go.broken"), b.Bytes(), 0o644)
		return fmt.Errorf("gofmt: %w\n%s", err, b.String())
	}

	out := filepath.Join(godotDir, "aliases.gen.go")
	return os.WriteFile(out, formatted, 0o644)
}
