package main

import (
	"bytes"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"strings"
)

// emitGlobalEnums writes <outDir>/enums/enums.gen.go with one Go type per
// entry in api.GlobalEnums. The underlying type is always int64 — bitfield
// enums share the same shape (Go has no native bitfield type; users compose
// values with `|`).
//
// Value names go SCREAMING_SNAKE → PascalCase verbatim, no prefix stripping.
// Stripping is fragile (Side/SIDE_LEFT works, Error/OK doesn't, KeyModifierMask
// has mixed shapes); pass-through preserves the host's distinctions and reads
// fine from Go (`enums.SideLeft`, `enums.Ok`, `enums.ErrUnavailable`).
//
// Enum names with a dot ("Variant.Type") drop the dot to form a single Pascal
// identifier ("VariantType").
func emitGlobalEnums(api *API, outDir string) error {
	enumsDir := filepath.Join(outDir, "enums")
	if err := os.MkdirAll(enumsDir, 0o755); err != nil {
		return err
	}

	var buf bytes.Buffer
	buf.WriteString("// Code generated by godot-go-bindgen. DO NOT EDIT.\n\n")
	buf.WriteString("// Package enums holds Godot's top-level enums and bitfields\n")
	buf.WriteString("// (Side, Key, Error, PropertyHint, …). Each is a typed int64; bitfield\n")
	buf.WriteString("// enums share the shape and are composed with the usual `|`.\n")
	buf.WriteString("package enums\n\n")

	totalValues := 0
	for i := range api.GlobalEnums {
		e := &api.GlobalEnums[i]
		typeName := globalEnumTypeName(e.Name)

		kindWord := "enum"
		if e.IsBitfield {
			kindWord = "bitfield"
		}
		fmt.Fprintf(&buf, "// %s mirrors Godot's %s %s.\n", typeName, e.Name, kindWord)
		fmt.Fprintf(&buf, "type %s int64\n\n", typeName)

		if len(e.Values) == 0 {
			continue
		}
		buf.WriteString("const (\n")
		for _, v := range e.Values {
			constName := pascal(strings.ToLower(v.Name))
			// A handful of Godot bitfield values share the literal name of
			// their parent type (e.g. KeyModifierMask.KEY_MODIFIER_MASK).
			// Go has no enum scoping at package level, so suffix the const
			// to break the collision. "Bits" reads naturally for bitfields,
			// "Value" for plain enums.
			if constName == typeName {
				if e.IsBitfield {
					constName += "Bits"
				} else {
					constName += "Value"
				}
			}
			fmt.Fprintf(&buf, "\t%s %s = %d\n", constName, typeName, v.Value)
		}
		buf.WriteString(")\n\n")
		totalValues += len(e.Values)
	}

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		broken := filepath.Join(enumsDir, "enums.gen.go.broken")
		_ = os.WriteFile(broken, buf.Bytes(), 0o644)
		return fmt.Errorf("gofmt enums: %w (raw at %s)", err, broken)
	}

	out := filepath.Join(enumsDir, "enums.gen.go")
	if err := os.WriteFile(out, formatted, 0o644); err != nil {
		return err
	}

	fmt.Fprintf(os.Stderr, "godot-go-bindgen: emitted %d enums (%d total constants)\n",
		len(api.GlobalEnums), totalValues)
	return nil
}

// globalEnumTypeName converts a global enum name to a Go type identifier.
// Dotted names ("Variant.Type") fold into one Pascal identifier
// ("VariantType"); bare names pass through unchanged.
func globalEnumTypeName(godotName string) string {
	if dot := strings.IndexByte(godotName, '.'); dot >= 0 {
		return godotName[:dot] + godotName[dot+1:]
	}
	return godotName
}
