package main

import (
	"fmt"
	"os"
	"path/filepath"
)

// emitVersion writes <bindings-dir>/version.gen.go with the user's
// compile-time view of the Godot API — the version data from the
// extension_api.json the bindings were generated against. The file's
// init() registers the version with the framework so
// internal/gdextension can warn on major/minor drift between
// bindings and the live host on first init.
//
// Lives in the user's package rather than internal/gdextension so the
// constants reflect THIS user's chosen Godot version (different users
// may target different versions); the framework runtime stays generic
// and queries the host independently.
func emitVersion(api *API, cfg *genConfig) error {
	path := filepath.Join(cfg.Dir, "version.gen.go")
	f, err := os.Create(path)
	if err != nil {
		return err
	}
	defer f.Close()
	h := api.Header
	_, err = fmt.Fprintf(f, `// Code generated by godot-go-bindgen. DO NOT EDIT.

package %s

import "%s/internal/gdextension"

// Constants describing the Godot extension API these bindings target.
// Sourced from extension_api.json's header block at bindgen time.
// The init() below registers them with the framework runtime so it
// can warn on major/minor drift between bindings and the live host
// on extension load.

const (
	// APIMajor / APIMinor / APIPatch identify the Godot release the
	// bindings were generated against. The framework checks
	// (Major, Minor) against the live host on first load and
	// surfaces a warning if they don't match — patch-level drift is
	// expected and tolerated.
	APIMajor = %d
	APIMinor = %d
	APIPatch = %d

	// APIVersion is the human-readable build tag from the JSON
	// header — for example, "Godot Engine v4.6.2.stable.official".
	// Useful in diagnostics and "godot --version" comparisons.
	APIVersion = %q

	// APIPrecision is "single" or "double" — the float build the
	// bindings were generated for. Mixing precisions between the
	// bindings and the host produces undefined behavior.
	APIPrecision = %q
)

func init() {
	gdextension.RegisterAPIVersion(APIMajor, APIMinor, APIPatch, APIVersion)
}
`, cfg.Package, cfg.ModulePath, h.VersionMajor, h.VersionMinor, h.VersionPatch, h.VersionFullName, h.Precision)
	return err
}
