package main

import (
	"fmt"
	"os"
	"path/filepath"
)

// emitVersion writes internal/gdextension/version.gen.go with the
// framework's compile-time view of the Godot API: the version data
// from extension_api.json's header. The first-init hook in the same
// package compares these constants against the live host's reported
// version and surfaces a warning on a major/minor mismatch.
//
// Lives in internal/gdextension/ rather than godot/ to avoid an
// import cycle (godot → core → internal/gdextension); users who
// want the version for diagnostics can import internal/gdextension
// the same way they import other framework helpers.
func emitVersion(api *API, outDir string) error {
	path := filepath.Join(outDir, "internal", "gdextension", "version.gen.go")
	f, err := os.Create(path)
	if err != nil {
		return err
	}
	defer f.Close()
	h := api.Header
	_, err = fmt.Fprintf(f, `// Code generated by godot-go-bindgen. DO NOT EDIT.

package gdextension

// Constants describing the Godot extension API the framework's
// generated bindings target. Sourced from extension_api.json's
// header block at bindgen time. The first-init hook in this
// package compares these against the live host on extension load
// and surfaces a warning on a major/minor mismatch.

const (
	// APIMajor / APIMinor / APIPatch identify the Godot release the
	// bindings were generated against. internal/gdextension checks
	// (Major, Minor) against the live host on first load and
	// surfaces a warning if they don't match — patch-level drift is
	// expected and tolerated.
	APIMajor = %d
	APIMinor = %d
	APIPatch = %d

	// APIVersion is the human-readable build tag from the JSON
	// header — for example, "Godot Engine v4.6.2.stable.official".
	// Useful in diagnostics and "godot --version" comparisons.
	APIVersion = %q

	// APIPrecision is "single" or "double" — the float build the
	// bindings were generated for. Mixing precisions between the
	// framework and the host produces undefined behavior; the
	// framework's runtime check rejects mismatches outright.
	APIPrecision = %q
)
`, h.VersionMajor, h.VersionMinor, h.VersionPatch, h.VersionFullName, h.Precision)
	return err
}
